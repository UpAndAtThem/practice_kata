MORSE_DICT = {
    "a" => ".-","b" => "-...","c" => "-.-.","d" => "-..","e" => ".","f" => "..-.","g" => "--.",
    "h" => "....","i" => "..","j" => ".---","k" => "-.-","l" => ".-..","m" => "--","n" => "-.",
    "o" => "---","p" => ".--.","q" => "--.-","r" => ".-.","s" => "...","t" => "-","u" => "..-",
    "v" => "...-","w" => ".--","x" => "-..-","y" => "-.--","z" => "--.."," " => " ","1" => ".----",
    "2" => "..---","3" => "...--","4" => "....-","5" => ".....","6" => "-....","7" => "--...",
    "8" => "---..","9" => "----.","0" => "-----"
}

def morse_decoder(morse_code)
  decoded_word = ''

  arr = morse_code.split(/   /).map do |word|
    word.split(" ").map do |char|
      MORSE_DICT.key(char)
    end 
  end

  arr.map(&:join).join(" ").upcase
end

